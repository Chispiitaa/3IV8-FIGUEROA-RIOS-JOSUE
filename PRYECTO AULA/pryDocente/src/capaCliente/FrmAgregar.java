    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int horD = 0;
        
        int horAct = Integer.parseInt(jHoraAct.getSelectedItem().toString());
        horD = Integer.parseInt(jHorasD.getText());
        int actI;
        String nomact = jNomTarea.getText();
        
        
        if(horD == 0 || nomact.equals("")){
            
            
            JOptionPane.showMessageDialog(null, "ALGUNOS CAMPOS ESTAN VACIOS");
            
        }else{
        
        if(Lunes.isSelected()){
            actI = 1;

            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Martes.isSelected()){
            actI = 2;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Miercoles.isSelected()){
            actI = 3;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Jueves.isSelected()){
            actI = 4;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Viernes.isSelected()){
            actI = 5;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Sabado.isSelected()){
            actI = 6;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
        
        if(Domingo.isSelected()){
            actI = 7;
            
            tab.AgregarTareas(horAct, actI, horD, nomact);
        }
       
        tab.setVisible(true);
        this.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

